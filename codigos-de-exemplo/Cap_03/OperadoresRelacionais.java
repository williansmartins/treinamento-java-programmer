
public class OperadoresRelacionais {
	public static void main(String args[]){
		int a = 20, b = 30, c = 1, d = 20;
		System.out.println(b < a);
		System.out.println(a == d);
		System.out.println(c > b);
		System.out.println(a != d);
		System.out.println(a < d);
		System.out.println(a <= d);
		System.out.println(c >= b);
	}
}
